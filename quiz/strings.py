# Python String Methods Challenge

# Given Data:
full_name = "John Doe"

# 1. Convert the entire name to uppercase and print it.
print(full_name.upper())
# 2. Convert the entire name to lowercase and print it.
print(full_name.lower())
# 3. Print the length of the name.
print(len(full_name))
# 4. Replace all spaces with underscores and print the result.
full_name_underscore = full_name.replace(" ", "_")
print(full_name_underscore)